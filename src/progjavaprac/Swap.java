package progjavaprac;

public class Swap {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		int c;
		System.out.println("Initial value of a is:"+a);
		System.out.println("Initial value of b is:"+b);
		c=a;
		a=b;
		b=c;
		
		System.out.println("Swapping value of a is:"+a);
		System.out.println("Swapping value of b is:"+b);

	}

}
